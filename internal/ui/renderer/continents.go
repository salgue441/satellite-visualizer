// IsLand returns true if the given lat/lon falls on a landmass.
// Uses ray-casting point-in-polygon test against continental outlines.
func IsLand(lat, lon float64) bool {
	for _, poly := range continents {
		if pointInPolygon(lat, lon, poly) {
			return true
		}
	}
	return false
}

// pointInPolygon uses the ray-casting algorithm to determine if a point
// is inside a polygon. Casts a horizontal ray and counts edge crossings.
func pointInPolygon(lat, lon float64, polygon []GeoPoint) bool {
	n := len(polygon)
	inside := false

	j := n - 1
	for i := 0; i < n; i++ {
		pi := polygon[i]
		pj := polygon[j]

		// Check if a horizontal ray from (lat, lon) going in +lon direction
		// crosses the edge from pj to pi
		if (pi.Lat > lat) != (pj.Lat > lat) {
			// Compute the longitude of the intersection
			intersectLon := pj.Lon + (lat-pj.Lat)/(pi.Lat-pj.Lat)*(pi.Lon-pj.Lon)
			if lon < intersectLon {
				inside = !inside
			}
		}
		j = i
	}

	return inside
}
