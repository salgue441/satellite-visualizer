// ContinentBBox holds precomputed bounding box for a continent polygon.
type ContinentBBox struct {
	MinLat, MaxLat, MinLon, MaxLon float64
}

var continentBBoxes []ContinentBBox

func init() {
	continentBBoxes = make([]ContinentBBox, len(continents))
	for i, poly := range continents {
		if len(poly) == 0 {
			continue
		}
		bb := ContinentBBox{
			MinLat: poly[0].Lat, MaxLat: poly[0].Lat,
			MinLon: poly[0].Lon, MaxLon: poly[0].Lon,
		}
		for _, p := range poly[1:] {
			if p.Lat < bb.MinLat {
				bb.MinLat = p.Lat
			}
			if p.Lat > bb.MaxLat {
				bb.MaxLat = p.Lat
			}
			if p.Lon < bb.MinLon {
				bb.MinLon = p.Lon
			}
			if p.Lon > bb.MaxLon {
				bb.MaxLon = p.Lon
			}
		}
		bb.MinLat -= 0.5
		bb.MaxLat += 0.5
		bb.MinLon -= 0.5
		bb.MaxLon += 0.5
		continentBBoxes[i] = bb
	}
}

// IsLand returns true if the given lat/lon falls on a landmass.
// Uses bounding-box pre-check and ray-casting point-in-polygon test.
func IsLand(lat, lon float64) bool {
	for i, poly := range continents {
		bb := continentBBoxes[i]
		if lat < bb.MinLat || lat > bb.MaxLat || lon < bb.MinLon || lon > bb.MaxLon {
			continue
		}
		if pointInPolygon(lat, lon, poly) {
			return true
		}
	}
	return false
}

// pointInPolygon uses the ray-casting algorithm to determine if a point
// is inside a polygon. Casts a horizontal ray and counts edge crossings.
func pointInPolygon(lat, lon float64, polygon []GeoPoint) bool {
	n := len(polygon)
	inside := false

	j := n - 1
	for i := 0; i < n; i++ {
		pi := polygon[i]
		pj := polygon[j]

		if (pi.Lat > lat) != (pj.Lat > lat) {
			intersectLon := pj.Lon + (lat-pj.Lat)/(pi.Lat-pj.Lat)*(pi.Lon-pj.Lon)
			if lon < intersectLon {
				inside = !inside
			}
		}
		j = i
	}

	return inside
}
